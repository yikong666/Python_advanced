"""
需求：验证在有释放锁的前提下，是否会发生死锁，什么情况会发生

步骤：
1.导包
2.创建锁
3.创建函数
4.上锁和解锁
5.创建子线程
6.启动线程
"""

# ==================== 1. 导包 ====================
import threading   # 线程模块，提供 Thread 类和 Lock 类
import time        # 时间模块，这里用于制造时间差，增加死锁出现的概率


# ==================== 2. 创建两把锁（两个共享资源） ====================
# 想象厨房里只有一口锅和一把铲子，谁拿到谁用，用完了才放回去
lock_guo = threading.Lock()    # 锁1：代表“锅”
lock_chan = threading.Lock()   # 锁2：代表“铲子”


# ==================== 3. 创建函数（线程要执行的任务） ====================

# 小明：炒菜的顺序是 —— 先拿锅，再拿铲子（先锁锅，再锁铲子）
def xiao_ming():
    print('小明准备拿锅')
    lock_guo.acquire()                          # ① 获得锅的锁（如果锅被别人拿着，会在这里阻塞等待）
    print("小明：拿到【锅】了，准备去拿【铲子】")
    time.sleep(0.1)                             # 造成死锁的原因
    lock_chan.acquire()                         # ② 尝试获得铲子的锁（此时小明已持有锅锁）
    print("小明：拿到【铲子】了，终于可以炒菜了！")
    lock_chan.release()                         # ③ 用完铲子，释放铲子锁
    lock_guo.release()                          # ④ 用完锅，释放锅锁


# 小红：炒菜的顺序是 —— 先拿铲子，再拿锅（先锁铲子，再锁锅）
def xiao_hong():
    print('小红准备拿铲子了')
    lock_chan.acquire()                         # ① 获得铲子的锁
    print("小红：拿到【铲子】，准备去拿【锅】")

    #time.sleep(0.1)                             # ② 故意等待0.1秒，让小明确实拿到锅锁
    lock_guo.acquire()                          # ③ 尝试获得锅的锁（此时小红已持有铲子锁）
    print("小红：拿到【锅】了，终于可以炒菜了！")  # 注意：原代码此处误写成“小明”，应为“小红”
    lock_guo.release()                          # ④ 释放锅锁
    lock_chan.release()                         # ⑤ 释放铲子锁


# ==================== 4. 主程序入口 ====================
if __name__ == '__main__':

    # 5. 创建两个子线程，分别模拟小明和小红
    t1 = threading.Thread(target=xiao_ming)   # 线程1：执行 xiao_ming 函数
    t2 = threading.Thread(target=xiao_hong)   # 线程2：执行 xiao_hong 函数

    # 6. 启动线程
    t1.start()   # 小明开始行动
    t2.start()   # 小红开始行动

    # 7. 等待两个线程结束（但实际上它们会死锁，永远无法结束）
    t1.join()
    t2.join()

    print("大家菜抄完了，手工！")   # 这行永远不会被打印，因为死锁导致线程无法终止