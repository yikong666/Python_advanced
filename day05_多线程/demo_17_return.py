import time


# ==========================================
# 场景一：使用 return (传统的做法：一次性全部给你)
# ==========================================
def bakery_return():
    """
    使用 return 的面包房：
    必须等所有蛋糕都烤完，打包成一个大盒子(列表)才能给你。
    """
    print("\n--- 📦 传统面包房 (使用 return) 开始营业 ---")
    box = []  # 准备一个大盒子装蛋糕

    for i in range(1, 4):
        print(f"👨‍🍳 厨师正在奋力烤第 {i} 个蛋糕...")
        time.sleep(1)  # 模拟烤蛋糕的时间
        box.append(f"🧁 蛋糕 {i}")

    print("🎁 厨师：所有蛋糕都烤完了，整盒端走吧！")
    return box  # 一次性返回装满蛋糕的盒子


# ==========================================
# 场景二：使用 yield (生成器的做法：做好一个给一个)
# ==========================================
def bakery_yield():
    """
    使用 yield 的流水线面包房：
    烤好一个就立刻交给你，然后厨师“暂停”工作，等你吃完来要下一个。
    """
    print("\n--- 🪄 魔法面包房 (使用 yield) 开始营业 ---")

    for i in range(1, 4):
        print(f"👨‍🍳 厨师正在精心烤第 {i} 个蛋糕...")
        time.sleep(1)  # 模拟烤蛋糕的时间

        # 重点来了！yield 会把刚刚烤好的蛋糕交出去，并且在这里“暂停”！
        yield f"🧁 蛋糕 {i}"

        # 当你再次向厨师索要蛋糕时，代码会从这里“继续”往下执行
        print(f"✅ 厨师：看你吃完了，我继续干活！")

    print("🛑 厨师：面糊用光了，今天打烊啦！")


# ==========================================
# 👨‍🏫 带着学生运行这里：观察两种方式的显著差异
# ==========================================
if __name__ == "__main__":

    # 1. 体验 return 的效果：你只能干等着，直到拿到一整盒
    print("【顾客 A 来到传统面包房】")
    cupcake_box = bakery_return()
    for cake in cupcake_box:
        print(f"😋 顾客 A 正在吃：{cake}")

    print("\n" + "=" * 40 + "\n")

    # 2. 体验 yield 的效果：拿到一个吃一个，互动交替进行
    print("【顾客 B 来到魔法面包房】")
    # 注意：调用 yield 的函数，拿到的不是蛋糕，而是一台“蛋糕生成机”
    cake_machine = bakery_yield()

    # 使用 for 循环不断向机器索要蛋糕
    for cake in cake_machine:
        print(f"🤩 顾客 B 拿到并马上吃掉：{cake}")
        print("⏳ 顾客 B 吃完了，催促厨师搞快点...\n")