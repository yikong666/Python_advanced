# =============================================================================
# 第六课：聚合统计、groupby 分组、pivot_table 透视表
#
# 【本课总流程 —— 建议板书】
#
#   ① 读入表 df
#        ↓
#   ② 不分组聚合（整表/整列一个数）→ 回答「一共几人、最高工资多少」
#        ↓
#   ③ groupby 分组聚合 → 回答「每个部门多少人、均薪多少」
#        ↓
#   ④ pivot_table 透视表 → 用报表形式呈现（与 ③ 本质类似，写法不同）
#
# 【本示例数据 emp.txt 读入后（5 名员工）】
#   技术部：张三5000、王五6000
#   销售部：李四7000、田七7500
#   人事部：赵六5500
#
# 【与 SQL 对照】
#   df['sal'].mean()                          →  SELECT AVG(sal) FROM emp;
#   df.groupby('dept')['sal'].mean()          →  SELECT dept, AVG(sal) FROM emp GROUP BY dept;
# =============================================================================

import pandas as pd


def show(title, result, principle="", process="", keywords=""):
    """
    教学打印四件套：
      principle → 为什么要这么做、背后思想
      process   → 计算机/ Pandas 内部分几步完成
      keywords  → 每个参数、关键字含义
      result    → 运行结果
    """
    print("\n" + "=" * 62)
    print(f"【{title}】")
    if principle:
        print("  ■ 原理:", principle)
    if process:
        print("  ■ 实现流程:", process)
    if keywords:
        print("  ■ 关键字:", keywords)
    print("  ■ 结果:")
    print(result)


# =============================================================================
# 步骤 0：读入数据 —— 一切分析的起点
# =============================================================================

# pd.read_csv：读取分隔文本为 DataFrame
# 'emp.txt'：相对路径，请在 03_pandas 目录下运行
# sep='-'：按短横线切分；第一行 name-age-dept-sal 自动作为列名（默认 header=0）
df = pd.read_csv('emp.txt', sep='-')

show(
    "步骤0：pd.read_csv('emp.txt', sep='-')",
    df,
    principle="先有表结构（行=员工，列=字段），才能做聚合；列名来自文件第一行",
    process="① 打开文件 → ② 按 sep 切每行 → ③ 第一行作 columns → ④ 其余行作数据",
    keywords="read_csv(路径, sep=分隔符)"
)

# =============================================================================
# 第一部分：不分组聚合（整表 / 整列归成一个数）
# 【场景】HR 问：公司一共几个员工？工资最高多少？平均工资？工资总额？
# 【原理】聚合 = 把很多行/很多格子「压缩」成一个统计量（sum/mean/max/count）
# =============================================================================

print("\n" + "#" * 62)
print("#  第一部分：不分组聚合")
print("#" * 62)

# -------------------------------------------------------------------------
# count()：统计每列「非空」单元格个数
# -------------------------------------------------------------------------
# 实现流程：
#   ① 逐列扫描
#   ② 每个格子判断是不是 NaN（缺失）
#   ③ 不是 NaN 的计数 +1
#   ④ 返回一个 Series：index=列名，value=该列非空个数
show(
    "df.count()",
    df.count(),
    principle="检查数据完整性；若某列 count < 行数，说明该列有缺失值",
    process="逐列计数非空 → 得到每列有效数据条数",
    keywords="count()：只数非 NaN；NaN 不参与任何聚合"
)
# 本表每列都是 5 → 5 名员工、无缺失

print("\n  --- 行数的三种写法（结果应都是 5）---")
print("  len(df)         :", len(df),
      "  ← len 对 DataFrame 默认统计「行数」")
print("  len(df.index)   :", len(df.index),
      "  ← index 是行标签序列，长度=行数")
print("  df.shape[0]     :", df.shape[0],
      "  ← shape 返回 (行数, 列数)，[0] 取行数")

# -------------------------------------------------------------------------
# max()：一列中的最大值
# -------------------------------------------------------------------------
# 实现流程：
#   ① df['sal'] 取出薪资列（Series，长度 5）
#   ② .max() 在 Series 上找最大元素（内部一次遍历比较）
# 手算：max(5000,7000,6000,5500,7500) = 7500（田七，销售部）
show(
    "df['sal'].max()",
    df['sal'].max(),
    principle="先选列再聚合；只对数字列有意义",
    process="① df['sal'] 得 Series → ② .max() 遍历比较 → ③ 返回标量 7500",
    keywords="df['列名']→Series；.max()→最大值（标量）"
)

# -------------------------------------------------------------------------
# 聚合 + 布尔筛选：先算 max，再筛出「工资等于 max」的行
# -------------------------------------------------------------------------
# 实现流程：
#   ① df['sal'].max()  → 7500（一个数）
#   ② df['sal'] == 7500 → 对每行 True/False：[F,F,F,F,T]
#   ③ df[布尔序列]     → 只保留 True 的行（田七那一行）
# 等价 SQL：SELECT * FROM emp WHERE sal = (SELECT MAX(sal) FROM emp);
show(
    "df[df['sal'] == df['sal'].max()]  最高薪员工",
    df[df['sal'] == df['sal'].max()],
    principle="聚合结果是一个数，可以和整列逐行比较，实现「找极值对应记录」",
    process="① 算 max → ② 列与标量 == → ③ 布尔索引筛行",
    keywords="df[条件]；== 逐元素比较"
)

# -------------------------------------------------------------------------
# mean()：算术平均
# -------------------------------------------------------------------------
# 公式：mean = (x1+x2+...+xn) / n
# 手算：(5000+7000+6000+5500+7500) / 5 = 31000/5 = 6200
show(
    "df['sal'].mean()",
    df['sal'].mean(),
    principle="整体平均水平；会被极端高薪/低薪拉偏",
    process="① sum 全列 → ② 除以非空个数 n → ③ 返回标量",
    keywords="mean()：算术平均"
)

# -------------------------------------------------------------------------
# sum()：求和
# -------------------------------------------------------------------------
# 手算：5000+7000+6000+5500+7500 = 31000（公司月薪总额）
show(
    "df['sal'].sum()",
    df['sal'].sum(),
    principle="财务「工资总包」；分组后也可用 groupby().sum() 看各部门总额",
    process="① 遍历该列所有数累加 → ② 返回标量",
    keywords="sum()：求和"
)

# =============================================================================
# 第二部分：groupby 分组聚合（本课核心）
#
# 【为什么要分组？】
#   全公司 mean=6200 看不出：技术部 5500、销售部 7250、人事部 5500
#   必须按 dept 拆成小组，组内再算 mean/count
#
# 【Split - Apply - Combine 三步模型】
#   Split   拆分：按 dept 把行分成若干堆（技术部一堆、销售部一堆…）
#   Apply   应用：每一堆内部执行 count / mean / max …
#   Combine 合并：把各堆的结果拼成一张小表（index=部门名）
# =============================================================================

print("\n" + "#" * 62)
print("#  第二部分：groupby 分组聚合（Split → Apply → Combine）")
print("#" * 62)

# -------------------------------------------------------------------------
# groupby('dept')['name'].count()  —— 各部门人数
# -------------------------------------------------------------------------
# 实现流程：
#   Split：  技术部={张三,王五}  销售部={李四,田七}  人事部={赵六}
#   Apply：  每组对 name 列 count → 技术部2人，销售部2人，人事部1人
#   Combine：index=部门，value=人数 → Series
show(
    "df.groupby('dept')['name'].count()",
    df.groupby('dept')['name'].count(),
    principle="分组键 dept 的值成为结果行的 index；统计每组有多少条员工记录",
    process="Split按dept分堆 → Apply对name计数 → Combine成Series(部门→人数)",
    keywords="groupby('分组列')：按该列相同值分组；['name']：在组内操作哪列；.count()：非空个数"
)

# -------------------------------------------------------------------------
# groupby('dept')['sal'].mean()  —— 各部门平均薪资
# -------------------------------------------------------------------------
# 手算：
#   技术部 mean = (5000+6000)/2 = 5500
#   销售部 mean = (7000+7500)/2 = 7250
#   人事部 mean = 5500/1 = 5500
show(
    "df.groupby('dept')['sal'].mean()",
    df.groupby('dept')['sal'].mean(),
    principle="每组单独算平均，才能比较部门间薪资差异",
    process="Split按dept → Apply对sal求mean → Combine",
    keywords="['sal']：聚合列；.mean()：组内平均"
)

# -------------------------------------------------------------------------
# .agg([...])  —— 一次算多个指标
# -------------------------------------------------------------------------
# 实现流程：
#   对每个部门、依次调用 max/min/sum/mean 四个函数
#   返回 DataFrame：行=部门，列=max/min/sum/mean
# 技术部 sal 手算：max=6000, min=5000, sum=11000, mean=5500
show(
    "df.groupby('dept')['sal'].agg(['max','min','sum','mean'])",
    df.groupby('dept')['sal'].agg(['max', 'min', 'sum', 'mean']),
    principle="避免写 4 次 groupby；适合部门薪资报告一张表出全",
    process="Split → Apply多个函数(agg列表) → Combine成多列DataFrame",
    keywords="agg=aggregate聚合；传入函数名列表['max','min','sum','mean']"
)

# =============================================================================
# 第三部分：pivot_table 透视表
#
# 【和 groupby 的关系】
#   本质都是：按某维度分组 + 对某列做聚合
#   groupby：程序员思维，链式调用
#   pivot_table：报表思维，指定「行维度、值列、聚合函数」
#
# 【典型流程】
#   指定 index（行上显示什么）→ values（对哪列算）→ aggfunc（怎么算）
# =============================================================================

print("\n" + "#" * 62)
print("#  第三部分：pivot_table 透视表")
print("#" * 62)

# -------------------------------------------------------------------------
# 各部门人数（与 groupby...count 同类结果，呈现为「行=部门」的表）
# -------------------------------------------------------------------------
show(
    "pivot_table(index='dept', values='name', aggfunc='count')",
    df.pivot_table(index='dept', values='name', aggfunc='count'),
    principle="把 dept 摆在行索引上，单元格=该部门 name 的非空计数",
    process="① 按 index=dept 分组 → ② 对 values=name 做 count → ③ 填进透视表格子",
    keywords="index=行维度(分组键)；values=参与计算的列；aggfunc='count'计数"
)

# -------------------------------------------------------------------------
# 各部门平均薪资
# -------------------------------------------------------------------------
show(
    "pivot_table(..., values='sal', aggfunc='mean')",
    df.pivot_table(index='dept', values='sal', aggfunc='mean'),
    principle="与 groupby('dept')['sal'].mean() 信息相同，列名呈现不同",
    process="分组dept → 组内 sal 求 mean → 输出一列",
    keywords="values='sal'：对工资列聚合；aggfunc='mean'"
)

# -------------------------------------------------------------------------
# 多指标：aggfunc 传列表
# -------------------------------------------------------------------------
# 结果列可能是多级列名 (max/min/sum/mean)，适合直接导出 Excel 汇报
show(
    "pivot_table(..., aggfunc=['max','min','sum','mean'])",
    df.pivot_table(index='dept', values='sal', aggfunc=['max', 'min', 'sum', 'mean']),
    principle="一次输出部门薪资四维指标；列名较多，需让学生对照列标题阅读",
    process="每个部门对 sal 连续算 4 种聚合 → 横向拼成多列",
    keywords="aggfunc=[函数列表]：多聚合函数"
)

# =============================================================================
# 【本课小结 —— 可让学生复述】
# =============================================================================
print("\n" + "=" * 62)
print("【本课小结】")
print("  1. 不分组：整列 .max() / .mean() / .sum() / df.count() → 一个公司总指标")
print("  2. 分组：groupby('部门')['工资'].mean() → 每组一个指标")
print("  3. 内核：Split(按组分堆) → Apply(组内算) → Combine(拼结果)")
print("  4. pivot_table：用 index/values/aggfunc 写法和 groupby 异曲同工")
print("  5. 找极值记录：先 .max() 再 df[df['sal']==max] 两步配合")
print("=" * 62)
