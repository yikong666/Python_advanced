# =============================================================================
# 广播（broadcasting）：形状不同的数组也能做 + - * / 等运算
# 规则简述：从最后一维对齐；长度相等或其一为 1；缺的维视为 1
# 目的：逻辑上“放大”小数组，避免真的复制大块内存
# =============================================================================

import numpy as np


def show(title, arr, note=""):
    print("\n" + "=" * 55)
    print(f"【{title}】")
    if note:
        print("  说明:", note)
    print("  shape:", arr.shape)
    print(arr)


# seed：固定随机种子，保证每次运行结果一致（教学演示用）
np.random.seed(666)

# randint(上界, size=形状)：生成 [0,上界) 的整数数组
my_array = np.random.randint(10, size=(3, 3))
show("主数组 my_array，shape (3,3)", my_array)

# shape (1,3)：一行三列；与 (3,3) 运算时沿 axis=0 复制 3 行 → 等价 (3,3)
a1 = np.array([[1, 2, 3]])
show("a1 一行三列", a1, "广播后每行都加 [1,2,3]")
#
# shape (3,1)：三行一列；沿 axis=1 复制 3 列 → 等价 (3,3)
a2 = np.array([[1], [2], [3]])
show("a2 三行一列", a2, "广播后第1列+1，第2列+2，第3列+3")

# ----- 广播演示 -----
# (3,3) + (1,3)：a1 在“行方向”扩展 3 次，逐元素相加
r1 = my_array + a1
show("my_array + a1", r1, "a1 从 (1,3) 广播成 (3,3)")

# (3,3) + (3,1)：a2 在“列方向”扩展 3 次
r2 = my_array + a2
show("my_array + a2", r2, "a2 从 (3,1) 广播成 (3,3)")

# 标量 3：视为 (1,1)，再广播到 (3,3)，每个元素都 +3
r3 = my_array + 3
show("my_array + 3", r3, "标量先变成 (1,1)，再铺满整个矩阵")
