# 判断下面列表中哪些是完整合法的 11 位手机号格式：
# phones = ["13800138000", "电话13800138000", "13800138000123", "13900139000"]

# 1.导包
import re

phones = ["13800138000", "电话13800138000", "13800138000123", "13900139000"]

# 遍历列表,逐个判断每个字符串是否符合手机号规则
for phone in phones:
    if re.match(r'^1\d{10}$', phone):
        print(phone)
        