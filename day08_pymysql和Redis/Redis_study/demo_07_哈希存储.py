import redis  # 导入 redis 库，用于连接和操作 Redis 数据库

# ==================== 1. 创建 Redis 连接对象 ====================
r = redis.Redis(
    host="localhost",  # Redis 服务器地址，localhost 表示本机
    port=6379,         # Redis 默认端口号 6379
    db=0,              # 使用的数据库编号（0-15），默认使用 0 号数据库
    decode_responses=True,  # 自动将 Redis 返回的字节串解码为字符串（方便阅读）
    protocol=2
)
# 注意：decode_responses=True 时，返回的键值对都是 str 类型；否则是 bytes 类型

# ==================== 2. 异常处理（确保最后关闭连接） ====================
try:
    # 定义存储用户信息的 Hash 结构的键名
    # 冒号 : 只是命名习惯（类似文件夹路径），用于按模块归类，方便调试和查看
    user_key = "day08:user:1001"  # 可以理解为：day08模块/user分类/id=1001

    # ==================== 3. 写入多个字段到 Hash ====================
    # hmset 命令：同时设置 hash 中的多个 field-value 对
    # 注意：在新版 redis-py (>=3.0) 中推荐使用 hset() 代替 hmset()，但此处 hmset 仍可用
    r.hmset(user_key, mapping={
        "name": "李四",   # field 字段名，value 字段值
        "age": "30",      # Redis 中存储的值都是字符串形式
        "city": "上海"
    })
    # mapping 参数：Python 字典，键为字段名，值为字段值

    # ==================== 4. 获取单个字段 ====================
    # hmget 方法：获取 hash 中一个或多个字段的值
    # 返回列表，即使只取一个字段也是列表形式
    print("用户名：", r.hmget(user_key, "name"))
    # 输出示例：用户名： ['李四']

    # ==================== 5. 获取整个 Hash 的所有字段和值 ====================
    # hgetall 方法：返回整个 hash 的 {字段名: 字段值} 字典
    # 注意：如果 hash 很大（包含数万个字段），此命令会阻塞 Redis，生产环境慎用
    print("完整用户信息：", r.hgetall(user_key))
    # 输出：完整用户信息： {'name': '李四', 'age': '30', 'city': '上海'}

    # ==================== 6. 按指定顺序获取多个字段 ====================
    # hmget 方法第二个参数使用列表指定需要获取哪些字段
    # 返回值的顺序与传入的字段列表顺序一致
    print("指定字段：", r.hmget(user_key, ["name", "city"]))
    # 输出：指定字段： ['李四', '上海']

    # ==================== 7. 获取所有字段名（不包含值） ====================
    # hkeys 方法：返回 hash 中所有 field 的列表
    # 适用于只需要字段名称，不需要值的场景（比如检查存在哪些属性）
    print("所有字段名：", r.hkeys(user_key))
    # 输出：所有字段名： ['name', 'age', 'city']

    # ==================== 8. 获取所有字段值（不包含字段名） ====================
    # hvals 方法：返回 hash 中所有 value 的列表
    # 顺序与 hkeys 返回的顺序一致，但不保证与插入顺序相同
    print("所有字段值：", r.hvals(user_key))
    # 输出：所有字段值： ['李四', '30', '上海']

    # ==================== 9. 删除一个字段 ====================
    # hdel 方法：删除 hash 中指定的一个或多个字段
    # 注意：删除字段后，该字段的键值对消失，但 hash 本身（user_key）依然存在
    # 只有删除最后一个字段后，hash 才变空，但仍占用极小的内存
    r.hdel(user_key, "city")
    print("删除 city 后：", r.hgetall(user_key))
    # 输出：删除 city 后： {'name': '李四', 'age': '30'}

# ==================== 10. 无论是否异常，最后都要关闭连接 ====================
finally:
    r.close()  # 关闭 Redis 连接，释放网络资源和连接池中的连接